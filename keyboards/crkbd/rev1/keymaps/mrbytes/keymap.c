#include QMK_KEYBOARD_H

void suspend_power_down_user(void) {
    #ifdef RGB_MATRIX_ENABLE
        rgb_matrix_set_suspend_state(true);
        rgb_matrix_disable();
    #endif
}

void suspend_wakeup_init_user(void) {
    #ifdef RGB_MATRIX_ENABLE
        rgb_matrix_set_suspend_state(false);
        rgb_matrix_enable();
    #endif
}

layer_state_t layer_state_set_user(layer_state_t state) {
    uint8_t layer = get_highest_layer(state);

    // Desactivar Caps Lock al cambiar a cualquier capa que no sea la base (0)
    if (layer != 0 && host_keyboard_led_state().caps_lock) {
        tap_code(KC_CAPS);  // Desactiva Caps Lock autom√°ticamente
    }

    switch (layer) {
        case 0:
            // Layer base - apagar efectos especiales
            rgb_matrix_mode(RGB_MATRIX_SOLID_COLOR);
            rgb_matrix_sethsv(HSV_TEAL);
            break;
        case 1:
            // Layer 1 - Red
            rgb_matrix_mode(RGB_MATRIX_SOLID_COLOR);
            rgb_matrix_sethsv(HSV_RED);
            break;
        case 2:
            // Layer 2 - Purple
            rgb_matrix_mode(RGB_MATRIX_SOLID_COLOR);
            rgb_matrix_sethsv(HSV_PURPLE);
            break;
        case 3:
            // Layer 3 - Yellow
            rgb_matrix_mode(RGB_MATRIX_SOLID_COLOR);
            rgb_matrix_sethsv(HSV_YELLOW);
            break;
    }
    return state;
}

// Para Caps Lock
bool led_update_user(led_t led_state) {
    if (led_state.caps_lock) {
        rgb_matrix_mode(RGB_MATRIX_SOLID_COLOR);
        rgb_matrix_sethsv(HSV_GREEN);
    } else {
        // Volver al color del layer actual
        layer_state_set_user(layer_state);
    }
    return true;
}

